package Game_LoosCoupleing;

public class Mario implements Game_Lanucher {

	public void up() {
		System.out.println("Mario Jump");
	}

	public void down() {
		System.out.println("Mario Roll");
	}

	public void left() {
		System.out.println("Mario Go Back");
		
	}

	public void right() {
		System.out.println("Mario Go Ahead");
	}

}
